int registro() // Função responsável por cadastrar os usuários no sistema 
{ 
    // início criação de variáveis/string
    char arquivo[40];
	char cpf[40];
    char nome[40];
    char sobrenome[40];
    char cargo[40]; 
    //final da criação de variáveis/string
    
    printf("Digite o CPF a ser cadastrado: "); // Coletando informação do usuário 
    scanf("%s", cpf); // %s Refere-se as string 
    
    strcpy(arquivo, cpf); // Responsável por copiar os valores das strings 
    
    FILE *file; // cria o arquivo 
    file = fopen(arquivo, "w"); // cria o arquivo e o "w" significa escrever 
    fprintf(file, cpf); //salva o valor da variável 
    fclose(file); // fecha arquivo 
    
    file = fopen(arquivo, "a"); 
    fprintf(file, ","); // Vírgula para separar as informações do usuário
    fclose(file);
    
    printf("Digite o nome a ser cadastrado: "); // Coletando informação do usuário 
    scanf("%s", nome); // Salvar a string 
    
    file = fopen(arquivo, "a"); // cria o arquivo 
    fprintf(file, nome); //salva o valor da variável 
    fclose(file); //fecha o arquivo 
    
    file = fopen(arquivo, "a"); 
    fprintf(file, ","); // Vírgula para separar as informações do usuário
    fclose(file);
    
    printf("Digite o sobrenome a ser cadastrado: "); //Coletanto informação do usuário 
    scanf("%s", sobrenome); //Salvar a string 
    
    file = fopen(arquivo, "a"); // cria o arquivo 
    fprintf(file, sobrenome); //salva o valor da variável 
    fclose(file); //fecha o arquivo 
    
    file = fopen(arquivo, "a"); 
    fprintf(file, ","); // vírgula para separar as informações do usuário 
    fclose(file); 
    
    printf("Digite o cargo a ser cadastrado: "); //Coletando informção do usuário 
    scanf("%s", cargo); //Salvar a string 
    
    file = fopen(arquivo, "a"); // cria o arquivo 
    fprintf(file, cargo); //salva o valor da variável 
    fclose(file); // fecha o arquivo 
    
    system("pause"); // pausar tela 
}

